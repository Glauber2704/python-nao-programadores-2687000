c = 5
d = 7

# Crie uma estrutura condicional para comparar dois números
if a > b:
  print(f'{a} é maior que {b}')
elif a > c:
  print(f'{a} é maior que {c}')
elif a < d:
  print(f'{a} é maior que {d}')
else:
  print(f'{a} não é maior que {b} nem {c} e nem {d}')
